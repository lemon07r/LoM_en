def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--import-root",
        type=Path,
        default=DEFAULT_IMPORT_ROOT,
        help="Path to the LegendOfMortalOverLlm checkout.",
    )
    args = parser.parse_args()
    import_root = args.import_root.resolve()
    if not import_root.exists():
        raise FileNotFoundError(import_root)

    llm_auto_path = import_root / "Files" / "Autotranslator" / "_AutoGeneratedTranslations.txt"
    llm_manual_path = import_root / "Files" / "ManualTranslations.yaml"
    glossary_path = import_root / "Files" / "Glossary.yaml"

    lom_candidates, lom_malformed = parse_xunity_file(LOM_TRANSLATION, "lom_static")
    llm_auto_candidates, llm_auto_malformed = parse_xunity_file(llm_auto_path, "llm_auto")
    llm_manual_candidates = yaml_candidates(llm_manual_path, "llm_manual")
    glossary_entry_candidates, glossary = glossary_candidates(glossary_path)

    all_candidates = lom_candidates + llm_auto_candidates + llm_manual_candidates + glossary_entry_candidates
    chosen, rejections, selection_stats = choose_xunity_candidates(all_candidates, glossary)
    xunity_validation = write_xunity_dictionary(chosen)
    stringtable_validation = build_stringtable(import_root)
    package_validation = build_zip()

    report = {
        "source_roots": {
            "base": ROOT.name,
            "import": import_root.name,
        },
        "source_git": {
            "base": git_metadata(ROOT),
            "import": git_metadata(import_root),
        },
        "source_entries": {
            "lom_static": len(lom_candidates),
            "llm_auto": len(llm_auto_candidates),
            "llm_manual": len(llm_manual_candidates),
            "llm_glossary": len(glossary_entry_candidates),
        },
        "xunity": {
            "malformed_source_lines": lom_malformed + llm_auto_malformed,
            "selection": selection_stats,
            "rejection_examples": [
                {
                    "source": rejection.source,
                    "line": rejection.line,
                    "reason": rejection.reason,
                    "key": rejection.key[:180],
                    "value": rejection.value[:180],
                }
                for rejection in rejections[:200]
            ],
            "validation": xunity_validation,
        },
        "stringtable": stringtable_validation,
        "quality_comparison": quality_comparison(all_candidates, chosen),
        "package": package_validation,
    }
    write_reports(report)

    failures = []
    if xunity_validation["malformed_lines"]:
        failures.append("final XUnity dictionary has malformed lines")
    if xunity_validation["duplicate_key_count"]:
        failures.append("final XUnity dictionary has duplicate keys")
    if xunity_validation["validation_failures"]:
        failures.append("final XUnity dictionary failed validation")
    if not stringtable_validation["row_count_matches_raw"]:
        failures.append("StringTable row count mismatch")
    if stringtable_validation["yaml_fragment_count"]:
        failures.append("StringTable contains YAML fragments")
    if package_validation["missing_required"]:
        failures.append("package is missing required paths")
    if not all(package_validation["config_checks"].values()):
        failures.append("AutoTranslator config checks failed")

    if failures:
        for failure in failures:
            print(f"ERROR: {failure}")
        print(f"See {REPORTS_DIR / 'merge_report.json'}")
        return 1

    print(f"Wrote {LOM_TRANSLATION.relative_to(ROOT)}")
    print(f"Wrote {STRINGTABLE_OUT.relative_to(ROOT)}")
    print(f"Wrote {REPORTS_DIR.relative_to(ROOT) / 'merge_report.md'}")
    print(f"Wrote {ZIP_OUT.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
