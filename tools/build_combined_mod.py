"""Build the combined Legend of Mortal English mod package.

The script imports structured translation data from the sibling
LegendOfMortalOverLlm checkout, merges it with this repository's XUnity
dictionary, validates the generated files, writes reports, and creates an
installable zip from Mod/.
"""

from __future__ import annotations

